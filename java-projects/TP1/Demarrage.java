
public class Demarrage{
    public static void main (String[]args) {
        
        byte myByte = 127;
        short MyShort = 1544;
        int MyInt = 24;
        long MyLong = 154L;
        boolean monboolean = true; /*Be carefule it's only "true" or "false" for a boolean*/
        char mychar = 'A';
        float myfloat = 15.5f;/*Don't forget the f*/
        double mydouble = 12.2d; /*Only d at the end*/

        System.out.println("My byte = " + myByte);
        System.out.println("My short =" + MyShort);
        System.out.println("My int =" + MyInt);
        System.out.println("My long = "+ MyLong);
        System.out.println("My boolean = " + monboolean);
        System.out.println("My char = " + mychar);
        System.out.println("My float = " + myfloat);
        System.out.println("My double = " + mydouble);    
    }
}